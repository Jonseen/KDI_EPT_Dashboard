          600 pre-election appeals in appellate court, says president
4th February 2023
Court Appeal, Abuja
Court of Appeal, Abuja.

Agency Report

The President of the Court of Appeal, Justice Monica Dongban-Mensem, on Friday, said the court had so far received 600 pre-election matters from its 20 Divisions.

Dongbam-Mensem made this known when the Ambassador of the European Union Delegation, Samuela Isopi, paid her a visit in her office.

“The court received over 600 pre-election appeals in the 20 divisions of the court from November to date and more appeals are still being filed.

“Over 60 panels were set up in the various divisions to hear and determine all pre-election appeals filed.
          </p>
          <p>
          “Mindful of the pressures under which the Hon Justices are working, some experienced registrars of the court were empanelled to summarise the decisions so far made and distribute to the various divisions to avoid conflicting decisions,” she said.

The president expressed joy at her presence which, she said, provided an opportunity to exchange views, share experiences and learn from their respective experiences in  their countries.

According to her, transparency in the electoral process is one of the core values of democratic stability in a nation.</p>
          
          <p>
          She stated that the active involvement of Election Observation Missions in Nigeria to ensure free, fair and credible elections  is appreciated.

“Pertinent to state that the role of these Observer Organisations have greatly impacted the resolve to review the Electoral Act, 2010.

“The Electoral Act, 2022 has introduced laudable innovations to strengthen our democracy and ensure credible elections.

“The Court of Appeal has reviewed, developed and expanded the scope of working materials for Judges,” she said.

According to her, there is now a Judicial Electoral Manual which incorporates decisions of the Supreme Court and those of the appellate court and the “critical” fundamental sections of the Electoral Act, 2022.

This, she said, is to ensure that appeals with similar facts and issues are categorised together to maintain the doctrine of stare decisis.
          </p>
