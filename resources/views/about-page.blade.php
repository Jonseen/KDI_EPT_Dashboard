  <h2 class="sub-about-heading">ABOUT THE ELECTION PETITION TRIBUNAL MONITORING PROJECT </h2>
  <p>The EPTM seeks to observe the Election Petition Tribunal (EPT) processes and 
    proceedings using the IFES election dispute resolution tracking methodology to 
    measure the tribunals’ compliance with all electoral legal frameworks, 
    Ultimately, put together the reports of EPTs in various states and make 
    recommendations available to the Court of Appeal (CoA) in order to further 
    reforms of the electoral justice system, improve the electoral justice processes 
    and further enhance efficiency. 
    KDI, with the support of IFES, a good and long-standing partner of the CoA, has been 
    monitoring this judicial process across 36 states, including the FCT, since 2019.  </p>
    <br>
    <hr>
    
    <small class="ssmall">The 2019 observations and recommendations made were instrumental in reviewing the 2022 Judicial Electoral Manual, as indicated on page 3 of the manual. </small>
    
